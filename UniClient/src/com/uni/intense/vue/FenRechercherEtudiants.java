void rechercher( String CodeEt){
try {
         
            // TODO add your handling code here:
 
        
            Context cont=new InitialContext();
            IEtudiants mato=(IEtudiants) cont.lookup(ser.getEtudiant());
             ArrayList<String> Fiche;
             Fiche=mato.FicheEtudiant(CodeEt);
             CodeEtud=Fiche.get(0);
             if(CodeEt.equals(CodeEtud)){
                
               
                txtCodeEtud.setText(Fiche.get(0));
                txtNom.setText(Fiche.get(1));
                txtPrenom.setText(Fiche.get(2));
                cmbSexe.setSelectedItem(Fiche.get(3));
                txtDate.setText(Fiche.get(4));               
                txtNif.setText(Fiche.get(5));                
                txtAdresse.setText(Fiche.get(6));
                txtEmail.setText(Fiche.get(7));
                txtPhone.setText(Fiche.get(8));
                txtNomRespon.setText(Fiche.get(9));
                txtPhoneRespon.setText(Fiche.get(10));
                txtVacation.setText(Fiche.get(11));
                txtCodOpt.setText(Fiche.get(12));
                txtpromo.setText(Fiche.get(13));
                txtniv.setText(Fiche.get(14));
                urlphoto=Fiche.get(15);
             
                
                if(urlphoto!=null){
                  TransfererFichier img=new TransfererFichier();
                  img.recevoirFichier(urlphoto);
                  afficherImage(urlphoto);
                  img.effacerFichierClient(urlphoto); 
                }
                else{
                 afficherImage(Images);
                }
                
                 PanGlobalEtud.setVisible(true);
                 bntModifierEtu.setVisible(true);
             }
             else{
                   PanGlobalEtud.setVisible(false);
         JOptionPane.showMessageDialog(null," le code de l'etudiant est Incorect","Erreur",JOptionPane.ERROR_MESSAGE);
            
             }
        }catch (NamingException ex) {
           Logger.getLogger(FenEnregisterEtudiants.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(FenRechercherEtudiants.class.getName()).log(Level.SEVERE, null, ex);
        } 

}
    private void bntModifierEtuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntModifierEtuActionPerformed
        // TODO add your handling code here:
        try {
         
            // TODO add your handling code here:
            String CodeEtud=txtCodeEtud.getText();
            if(CodeEtud.isEmpty()){
                JOptionPane.showMessageDialog(null,"Entrer le code de l'etudiant","Erreur",JOptionPane.ERROR_MESSAGE);
            }
            else{

                String Nom=txtNom.getText().trim().replace("'", "''");
                String Prenom =txtPrenom.getText().trim().replace("'", "''");

                String Adresse =txtAdresse.getText().trim().replace("'", "''");
                String Email =txtEmail.getText().trim().replace("'", "''");
                String Phone =txtPhone.getText();
                String NomResponsable =txtNomRespon.getText().trim().replace("'", "''");
                String PhoneResponsable =txtPhoneRespon.getText();
   boolean verif=s.isValid(Email);
                if(Nom.isEmpty()||Prenom.isEmpty()||Adresse.isEmpty()||Email.isEmpty()||NomResponsable.isEmpty()){
                    JOptionPane.showMessageDialog(null,"Tous les champs doivent etres remplis","Erreur",JOptionPane.ERROR_MESSAGE);
                }else if(Nom.length()<2||Prenom.length()<2||NomResponsable.length()<2){
                    JOptionPane.showMessageDialog(null,"les champs noms et prenoms et Nom des responsable ne doivent pas inferieur a un caractere","Erreur",JOptionPane.ERROR_MESSAGE);
                }
                else if(Phone.length()<13||PhoneResponsable.length()<13){
                JOptionPane.showMessageDialog(null,"Les Telephones doivent avoir plus de avoir 8 chiffres","Erreur",JOptionPane.ERROR_MESSAGE);
            }
                 else if(verif==false){
                JOptionPane.showMessageDialog(null,"Cet Email n'est pas valide","Erreur",JOptionPane.ERROR_MESSAGE);
            }
                else{
                    Context cont=new InitialContext();
                    IEtudiants mato=(IEtudiants) cont.lookup(ser.getEtudiant());
                    String gu= mato.ModifierEtudiant(CodeEtud, Nom, Prenom, Adresse, Email, Phone, NomResponsable, PhoneResponsable);
                    JOptionPane.showMessageDialog(FenRechercherEtudiants.this,gu);
                    rechercher(CodeEtud);
                }
            }
        }catch (NamingException ex) {
            Logger.getLogger(FenEnregisterEtudiants.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(FenRechercherEtudiants.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bntModifierEtuActionPerformed

