void rempliroption() throws NamingException, RemoteException{
  
                    ArrayList<String> Al=new ArrayList<>();
                    Context cont=new InitialContext();
                  InterfaceService mato=(InterfaceService) cont.lookup(ser.getService());
                    Al=  mato.remplirComboOptions();
                     int i=0;
                     cbxoptioncours.removeAllItems();
        for(String dep:Al)
        {
           
            cbxoptioncours.addItem(dep);
            
            i++;
        } 
}
    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        try {
            // TODO add your handling code here:
            String nomoption=txtnomoption.getText().trim().replace("'", "''");
             if(nomoption.isEmpty()){
                    JOptionPane.showMessageDialog(null,"Le nom de l'option est Vide","Erreur",JOptionPane.ERROR_MESSAGE);
            }
             else{
            
            Context cont=new InitialContext();
            InterfaceService mato=(InterfaceService) cont.lookup(ser.getService());
            String mes=mato.EnregistrerOption(nomoption);
            JOptionPane.showMessageDialog(PanGestionOPtionsCours.this,mes);
            listerOptions();
            rempliroption();
             }
        } catch (NamingException ex) {
            Logger.getLogger(PanGestionOPtionsCours.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(PanGestionOPtionsCours.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAjouterActionPerformed

