    private void btnajcoursActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnajcoursActionPerformed
        try {
            // TODO add your handling code here:
               
            String Nom_Opt=cbxoptioncours.getSelectedItem().toString();
            String Niveau=cbxniveau.getSelectedItem().toString();
            
            String Session=cbxsession.getSelectedItem().toString();
            String Nomcours=txtnomcours.getText().trim().replace("'", "''");
            if(Nomcours.isEmpty()){
                    JOptionPane.showMessageDialog(null,"Le nom du cours est Vide","Erreur",JOptionPane.ERROR_MESSAGE);
            }
            else{
            Context cont=new InitialContext();
            InterfaceService mato=(InterfaceService) cont.lookup(ser.getService());
            String mes=mato.EnregistrerCours(Nom_Opt, Niveau, Session,Nomcours);
            JOptionPane.showMessageDialog(PanGestionOPtionsCours.this,mes);
                  listerCours();
            }
        } catch (NamingException | RemoteException ex) {
            Logger.getLogger(PanGestionOPtionsCours.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnajcoursActionPerformed

